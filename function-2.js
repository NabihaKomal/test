/* Name Cases: Store a person name in a variable,
and than print that person name in lowercase or upercase, and titlecase.
*/
function changeLetterCase() {
    var personname = 'naBiha koMal';
    console.log("Name : ".concat(personname));
    console.log("Name in lowercase: ".concat(personname.toLowerCase()));
    console.log("Name in uppercase: ".concat(personname.toUpperCase()));
}
changeLetterCase();
